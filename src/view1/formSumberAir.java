    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
                                       
        try {
            if (jTextField3.getText().trim().isEmpty() ||
                jComboBox2.getSelectedItem() == null ||
                jTextField2.getText().trim().isEmpty() ||
                jComboBox1.getSelectedItem() == null) {

                javax.swing.JOptionPane.showMessageDialog(this,
                    "Semua field termasuk status harus diisi!",
                    "Input Tidak Lengkap",
                    javax.swing.JOptionPane.WARNING_MESSAGE);
                return;
            }

            dao.sumberAirDao dao = new dao.sumberAirDao();
            String namaSumber = jTextField3.getText().trim();

            if (dao.isNamaSumberExist(namaSumber)) {
                javax.swing.JOptionPane.showMessageDialog(this,
                    "Nama sumber \"" + namaSumber + "\" sudah terdaftar!\nTidak boleh duplikat.",
                    "Data Duplikat",
                    javax.swing.JOptionPane.WARNING_MESSAGE);
                return;
            }

            model.sumberAir sumber = new model.sumberAir();
            sumber.setNamaSumber(namaSumber);
            sumber.setJenisSumber((String) jComboBox2.getSelectedItem());
            sumber.setStatus((String) jComboBox1.getSelectedItem());
            sumber.setLokasi(jTextField2.getText().trim());

            dao.insert(sumber);

            javax.swing.JOptionPane.showMessageDialog(this,
                "Sumber air baru berhasil ditambahkan!");

            muatDataSumberAir();

            jTextField3.setText("");
            jComboBox2.setSelectedIndex(-1);
            jTextField2.setText("");
            jComboBox1.setSelectedIndex(-1);
        } catch (SQLException ex) {
            logger.log(java.util.logging.Level.SEVERE,
                "Kesalahan saat menambah sumber air", ex);
            javax.swing.JOptionPane.showMessageDialog(this,
                "Terjadi kesalahan saat menambah sumber air.\n" + ex.getMessage(),
                "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        try {
            int selectedRow = jTable1.getSelectedRow();
            if (selectedRow == -1) {
                javax.swing.JOptionPane.showMessageDialog(this,
                    "Pilih data yang ingin diubah!");
                return;
            }

            if (jTextField3.getText().trim().isEmpty() ||
                jComboBox2.getSelectedItem() == null ||
                jTextField2.getText().trim().isEmpty() ||
                jComboBox1.getSelectedItem() == null) {

                javax.swing.JOptionPane.showMessageDialog(this,
                    "Semua field termasuk status harus diisi!",
                    "Input Tidak Lengkap",
                    javax.swing.JOptionPane.WARNING_MESSAGE);
                return;
            }

            int id = (int) jTable1.getValueAt(selectedRow, 0);
            model.sumberAir sumber = new model.sumberAir();
            sumber.setIdSumber(id);
            sumber.setNamaSumber(jTextField3.getText().trim());
            sumber.setJenisSumber((String) jComboBox2.getSelectedItem());
            sumber.setStatus((String) jComboBox1.getSelectedItem());
            sumber.setLokasi(jTextField2.getText().trim());

            dao.sumberAirDao dao = new dao.sumberAirDao();
            dao.update(sumber);

            javax.swing.JOptionPane.showMessageDialog(this,
                "Data sumber air berhasil diperbarui!");

            muatDataSumberAir();

            jTextField3.setText("");
            jComboBox2.setSelectedIndex(-1);
            jTextField2.setText("");
            jComboBox1.setSelectedIndex(-1);
            jTable1.clearSelection();

        } catch (SQLException ex) {
            logger.log(java.util.logging.Level.SEVERE,
                "Kesalahan saat memperbarui data", ex);
            javax.swing.JOptionPane.showMessageDialog(this,
                "Terjadi kesalahan saat memperbarui data.\n" + ex.getMessage(),
                "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Pilih data yang ingin dihapus!");
            return;
        }

        int id = (int) jTable1.getValueAt(selectedRow, 0);
        int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(this,
            "Yakin ingin menghapus data ini?",
            "Konfirmasi Hapus",
            javax.swing.JOptionPane.YES_NO_OPTION);

        if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
            try {
                dao.sumberAirDao dao = new dao.sumberAirDao();
                dao.delete(id);

                javax.swing.JOptionPane.showMessageDialog(this,
                    "Data sumber air berhasil dihapus!");

                muatDataSumberAir();

                jTextField3.setText("");
                jComboBox2.setSelectedIndex(-1);
                jTextField2.setText("");
                jComboBox1.setSelectedIndex(-1);
                jTable1.clearSelection();

            } catch (SQLException ex) {
                logger.log(java.util.logging.Level.SEVERE,
                    "Kesalahan saat menghapus data", ex);
                javax.swing.JOptionPane.showMessageDialog(this,
                    "Terjadi kesalahan saat menghapus data.\n" + ex.getMessage(),
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_jButton4ActionPerformed

