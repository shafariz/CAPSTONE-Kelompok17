    private void muatDataSumberAir() {
    try {
        dao.sumberAirDao dao = new dao.sumberAirDao();
        java.util.List<model.sumberAir> daftarSumber = dao.getAll();

        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); 

        for (model.sumberAir s : daftarSumber) {
            model.addRow(new Object[]{
                s.getIdSumber(),
                s.getNamaSumber(),
                s.getJenisSumber(),
                s.getStatus(),
                s.getLokasi()
            });
        }

        jTextField3.setText("");
        jComboBox2.setSelectedIndex(-1);
        jTextField2.setText("");
        jComboBox1.setSelectedIndex(-1);
        jTable1.clearSelection();
        
        javax.swing.table.TableColumnModel kolom = jTable1.getColumnModel();
        kolom.getColumn(0).setPreferredWidth(40);   
        kolom.getColumn(1).setPreferredWidth(150);  
        kolom.getColumn(2).setPreferredWidth(80);  
        kolom.getColumn(3).setPreferredWidth(90); 
        kolom.getColumn(4).setPreferredWidth(120);  

    jTable1.getTableHeader().setResizingAllowed(false);
    jTable1.getTableHeader().setReorderingAllowed(false);

    javax.swing.table.TableRowSorter<javax.swing.table.DefaultTableModel> sorter = 
            new javax.swing.table.TableRowSorter<>(model);
        jTable1.setRowSorter(sorter);
    
    } catch (SQLException ex) {
        logger.log(java.util.logging.Level.SEVERE, "Gagal memuat data sumber air", ex);
        javax.swing.JOptionPane.showMessageDialog(this,
            "Gagal memuat data sumber air.\n" + ex.getMessage(),
            "Kesalahan Database",
            javax.swing.JOptionPane.ERROR_MESSAGE);
    }
}

    
