        private void muatDataLaporan() {
           laporanDao dao = new laporanDao();
           List<model.laporan1> daftarLaporan = dao.getAll();

           DefaultTableModel model = new DefaultTableModel(
               new Object[]{"ID", "Nama Warga", "Kabupaten/Kota", "Kecamatan", "Tanggal Laporan", "Deskripsi", "Status"}, 0
           ) {
               @Override
                public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        for (model.laporan1 l : daftarLaporan) {
            model.addRow(new Object[]{
                l.getIdLaporan(),
                l.getNamaPengguna(),
                l.getKotaKabupaten(),
                l.getKecamatan(),
                l.getTanggalLaporan(),
                l.getDeskripsi(),
                l.getStatus()
            });
        }

        jTable1.setModel(model);
        jTable1.setAutoCreateRowSorter(true); 
        
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(90);  
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(140);  
        jTable1.getColumnModel().getColumn(2).setPreferredWidth(150);  
        jTable1.getColumnModel().getColumn(3).setPreferredWidth(150);  
        jTable1.getColumnModel().getColumn(4).setPreferredWidth(180);  
        jTable1.getColumnModel().getColumn(5).setPreferredWidth(200);  
        jTable1.getColumnModel().getColumn(6).setPreferredWidth(130);  
    }

