    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                                   
    int selectedRow = jTable1.getSelectedRow();

    Object statusObj = jTable1.getValueAt(selectedRow, 6);
    Object kabupatenObj = jTable1.getValueAt(selectedRow, 2);
    Object kecamatanObj = jTable1.getValueAt(selectedRow, 3);

    String status = statusObj != null ? statusObj.toString() : "-";
    String kabupatenLaporan = kabupatenObj != null ? kabupatenObj.toString() : "-";
    String kecamatanLaporan = kecamatanObj != null ? kecamatanObj.toString() : "-";

    if (kabupatenLaporan.equals("-") || kecamatanLaporan.equals("-")) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Laporan ini tidak memiliki data daerah lengkap, tidak bisa ditindak.",
            "Data Tidak Lengkap",
            javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }

    if (status.equalsIgnoreCase("Sudah Ditindak")) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Laporan ini sudah ditindak sebelumnya.",
            "Informasi",
            javax.swing.JOptionPane.INFORMATION_MESSAGE);
        return;
    }


    dao.adminDao daoAdmin = new dao.adminDao();
    model.daerah daerahAdmin = daoAdmin.getDaerahAdmin(menuAdmin.idAdminLogin);

    if (daerahAdmin == null) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Gagal memuat data daerah admin.",
            "Error",
            javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    if (!kabupatenLaporan.equalsIgnoreCase(daerahAdmin.getKotaKabupaten()) ||
        !kecamatanLaporan.equalsIgnoreCase(daerahAdmin.getKecamatan())) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Anda tidak dapat menindak laporan dari daerah lain.",
            "Akses Ditolak",
            javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }


int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(this,
    "Laporan ini berasal dari daerah Anda.\nApakah Anda ingin menindak lanjut laporan ini?",
    "Konfirmasi Tindak",
    javax.swing.JOptionPane.YES_NO_OPTION,
    javax.swing.JOptionPane.QUESTION_MESSAGE);

if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
        try {
            int idLaporan = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());
            String namaWarga = jTable1.getValueAt(selectedRow, 1).toString();
            String kabupaten = jTable1.getValueAt(selectedRow, 2).toString();
            String kecamatan = jTable1.getValueAt(selectedRow, 3).toString();
            
            view1.formDistribusiAir form = new view1.formDistribusiAir();
            form.setDataLaporan(idLaporan, namaWarga, kabupaten, kecamatan);
            form.setVisible(true);
            this.dispose();
        } catch (SQLException ex) {
            System.getLogger(kelolaLaporan.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

