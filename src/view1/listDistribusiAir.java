    private void muatDataDistribusi() {
    try {
        distribusiAirDao distribusiDao = new distribusiAirDao();
        sumberAirDao sumberDao = new sumberAirDao();
        daerahDao daerahDao = new daerahDao();

        int idDaerahWarga = menuWarga.idDaerahWarga;
        System.out.println("üü¢ ID daerah warga login: " + idDaerahWarga);

        List<distribusiAir> daftarDistribusi = distribusiDao.getByDaerah(idDaerahWarga);
        System.out.println("üìã Jumlah distribusi ditemukan: " + daftarDistribusi.size());

        DefaultTableModel model = new DefaultTableModel(
            new Object[]{"ID", "Nama Sumber", "Kecamatan", "Tanggal Distribusi", "Volume Distribusi"}, 0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        for (distribusiAir d : daftarDistribusi) {
            String namaSumber = "-";
            String kecamatan = "-";

            try {
                sumberAir sumber = sumberDao.getById(d.getIdSumber());
                if (sumber != null) {
                    namaSumber = sumber.getNamaSumber();
                }

                daerah daerahObj = daerahDao.getById(d.getIdDaerah());
                if (daerahObj != null) {
                    kecamatan = daerahObj.getKecamatan();
                }
            } catch (SQLException e) {
                System.err.println("‚ö†Ô∏è Gagal memuat detail distribusi: " + e.getMessage());
            }

            model.addRow(new Object[]{
                d.getIdDistribusi(),
                namaSumber,
                kecamatan,
                d.getTanggalDistribusi(),
                (long) d.getVolumeDistribusi()
            });
        }

        jTable1.setModel(model);
        jTable1.getTableHeader().setReorderingAllowed(false);
        jTable1.getTableHeader().setResizingAllowed(false);

        javax.swing.table.TableRowSorter<javax.swing.table.DefaultTableModel> sorter =
                new javax.swing.table.TableRowSorter<>(model);
        jTable1.setRowSorter(sorter);

    } catch (SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(
            this,
            "Gagal memuat data distribusi: " + e.getMessage(),
            "Kesalahan Database",
            javax.swing.JOptionPane.ERROR_MESSAGE
        );
    }
}

